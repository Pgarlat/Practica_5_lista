package com.example.practica_5_lista;

public class GrupoMusical {

    private String title;

    private String image;

    public GrupoMusical(String title, String image) {
        this.title = title;
        this.image = image;
    }
    public String getTitle() {
        return title;
    }

    public String getImage() {
        return image;
    }
}
